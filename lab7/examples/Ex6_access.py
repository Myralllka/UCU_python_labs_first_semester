events =  ['1 січня', '2 січня', '3 січня', '4 січня', '5 січня']
print("events        =", events)

# non-negative indexes
print("events[0]     =", events[0])
print("events[2]     =", events[2])

# negative indexes
print("events[-1]    =", events[-1])
print("events[-3]    =", events[-3])

# slices events[start:end:step]
print("events[0:2]   =", events[0:2])
print("events[1:4]   =", events[1:4])
print("events[1:6:2] =", events[1:6:2])
