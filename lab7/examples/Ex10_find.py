# Check for list membership
a = [ 2, 3, 5, 2, 6, 2, 2, 7 ]
print("a      =", a)
print("2 in a =", (2 in a))
print("4 in a =", (4 in a))

# Check for list non-membership
print("a          =", a)
print("2 not in a =", (2 not in a))
print("4 not in a =", (4 not in a))

# Count occurrences in list
print("a          =", a)
print("a.count(1) =", a.count(1))
print("a.count(2) =", a.count(2))
print("a.count(3) =", a.count(3))
